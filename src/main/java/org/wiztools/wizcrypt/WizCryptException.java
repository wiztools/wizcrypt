package org.wiztools.wizcrypt;

/**
 *
 * @author subhash
 */
public class WizCryptException extends Exception {

    public WizCryptException() {
    }

    public WizCryptException(String message) {
        super(message);
    }

    public WizCryptException(Throwable cause) {
        super(cause);
    }

    public WizCryptException(String message, Throwable cause) {
        super(message, cause);
    }
    
}
